

# Install


It requires [Remember The Milk](https://www.rememberthemilk.com) account

add `rofi-rtm.sh` to your $PATH
install rtm-cli `npm -g install rtm-cli`

run `rtm` in your bash console, type `login` proceed as instructed, type `exit` when done

# Usage

Run this in your command line for test: 

```
rofi -show rtm -modi rtm:rofi-rtm.sh
```

type in `task added through rofi #tests`

check in your remember the milk app that the task appeared

bind rofi to your some keycombination and enjoy
